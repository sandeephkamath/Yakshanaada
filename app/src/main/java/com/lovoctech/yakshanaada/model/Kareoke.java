package com.lovoctech.yakshanaada.model;

public class Kareoke {

    private Shruthi shruthi;
    private String taala;
    private int kaala;
    private String nade;
    private String bidthige;

    public Shruthi getShruthi() {
        return shruthi;
    }

    public void setShruthi(Shruthi shruthi) {
        this.shruthi = shruthi;
    }

    public String getTaala() {
        return taala;
    }

    public void setTaala(String taala) {
        this.taala = taala;
    }

    public int getKaala() {
        return kaala;
    }

    public void setKaala(int kaala) {
        this.kaala = kaala;
    }

    public String getNade() {
        return nade;
    }

    public void setNade(String nade) {
        this.nade = nade;
    }

    public String getBidthige() {
        return bidthige;
    }

    public void setBidthige(String bidthige) {
        this.bidthige = bidthige;
    }
}
